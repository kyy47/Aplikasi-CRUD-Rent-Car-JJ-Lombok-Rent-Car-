    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        if (tf_id_mobil.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, id mobil belum diisi!");
            tf_id_mobil.requestFocus();
        }else if (tf_nama_mobil.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, nama mobil belum diisi!");
            tf_nama_mobil.requestFocus();   
        }else if (tf_tipe_mobil.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, tipe mobil belum diisi!");
            tf_nama_mobil.requestFocus();   
        }else if (tf_no_polisi.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, no polisi belum diisi!");
            tf_no_polisi.requestFocus();   
        }else if (tf_harga.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, harga belum diisi!");
            tf_harga.requestFocus();   
        }else{
            try {
                yo.setIdMobil(tf_id_mobil.getText());
                yo.setNamaMobil(tf_nama_mobil.getText());
                yo.setTipeMobil(tf_tipe_mobil.getText());
                yo.setNoPolisi(tf_no_polisi.getText());
                yo.setHarga(Integer.valueOf(tf_harga.getText()));
                yo.setKeterangan(cbo_keterangan.getSelectedItem().toString());
                yo.tambahData(yo.getIdMobil(), yo.getNamaMobil(), yo.getTipeMobil(), yo.getNoPolisi(), yo.getHarga(), yo.getKeterangan());

                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "Informasi",
                JOptionPane.INFORMATION_MESSAGE);
                tampil_database();
                reset_text();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Tersimpan", "Informasi", 
                JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

