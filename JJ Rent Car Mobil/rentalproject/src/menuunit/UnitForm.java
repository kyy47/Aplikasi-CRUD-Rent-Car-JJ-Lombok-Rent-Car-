      public void tampil_database() {
        Object[] baris = {"id_mobil", "nama_mobil", "tipe_mobil","no_polisi","harga","keterangan"};
        tabmode = new DefaultTableModel(null, baris);
        table_unit.setModel(tabmode);
        try {
            hasil = yo.tampilData();
            while (hasil.next()) {
                yo.setIdMobil(hasil.getString("id_mobil"));
                yo.setNamaMobil(hasil.getString("nama_mobil"));
                yo.setTipeMobil(hasil.getString("tipe_mobil"));
                yo.setNoPolisi(hasil.getString("no_polisi"));
                yo.setHarga(hasil.getInt("harga"));
                yo.setKeterangan(hasil.getString("keterangan"));
                String[] data = {yo.getIdMobil(), yo.getNamaMobil(), yo.getTipeMobil(), yo.getNoPolisi(), String.valueOf(yo.getHarga()), yo.getKeterangan()};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            // TODO: handle exception
        }
    }
    
