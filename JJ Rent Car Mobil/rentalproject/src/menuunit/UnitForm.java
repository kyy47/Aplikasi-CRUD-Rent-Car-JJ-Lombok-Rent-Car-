    private void tfIdMobilPencarianKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfIdMobilPencarianKeyReleased
        // TODO add your handling code here:
         try{
            st = CRUDkoneksi.createStatement();
            tabmode.getDataVector().removeAllElements();
            tabmode.fireTableDataChanged();
            ResultSet rs = st.executeQuery("SELECT * FROM unit WHERE id_mobil like '%" + tfIdMobilPencarian.getText() + "%' or nama_mobil like '%" + tfIdMobilPencarian.getText() + "%' or tipe_mobil like '%" + tfIdMobilPencarian.getText() + "%' or no_polisi like '%" + tfIdMobilPencarian.getText() + "%' " + "or harga like '%" + tfIdMobilPencarian.getText() + "%' or keterangan like '%" + tfIdMobilPencarian.getText() + "%'");
            while(rs.next()){
                Object[] data = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6)
                };
                tabmode.addRow(data);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_tfIdMobilPencarianKeyReleased


